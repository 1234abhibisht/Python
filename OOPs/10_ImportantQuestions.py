# Q-2)  What is difference in == and is
#     -> '==' compares values, while 'is' compare memory location

# Ex ->
x = 3
y = 3
print(x == y)  # True
print(x is y)  # True

x += 1
print(x == y)
print(x is y)  # False,  as now x and y does not have same memory





