# polymorphism means 'many forms'
# polymorphism enables objects of different classes to be treated as objects of a common superclass.
# means, same method name behaves differently in different situations

# compile time polymorphism is known as method overloading (not possible in python)
# runtime polymorphism is known as method overriding


# Why use Polymorphism?
# To write common code for different object types.
# To make programs more flexible.
# To simplify complex logic.